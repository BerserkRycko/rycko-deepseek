// Función para obtener artículos por idioma
export const getArticlesByLanguage = (language) => {
  return articles[language] || articles.en;
};

// Función para obtener un artículo específico
export const getArticleBySlug = (slug, language) => {
  const languageArticles = articles[language] || articles.en;
  return languageArticles.find(article => article.slug === slug);
};

// Función para obtener artículos destacados
export const getFeaturedArticles = (language) => {
  const languageArticles = articles[language] || articles.en;
  return languageArticles.filter(article => article.featured);
};